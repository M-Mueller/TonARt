#include "JumpingNote.h"

JumpingNote::JumpingNote(cv::Vec3f s, cv::Vec3f d)
: currentPos(s), direction(d), speed(0.02), liveTime(0), maxLiveTime(1), lastDraw(0)
{
	mesh = new Mesh(this);
	mesh->init();
}

JumpingNote::~JumpingNote()
{

}

void JumpingNote::draw()
{
	glMatrixMode(GL_MODELVIEW);
	glPushMatrix();

	glTranslatef(currentPos[0], currentPos[1], currentPos[2]);
	//glutSolidSphere(0.05, 8, 8);
	glScalef(0.01, 0.01, 0.01);
	mesh->draw();

	glPopMatrix();

	currentPos += (direction * speed);
	if(lastDraw>0)
	{
		liveTime+=time(NULL) - lastDraw;
	}
	lastDraw=time(NULL);
}

bool JumpingNote::isDead()
{
	return liveTime>maxLiveTime;
}

int JumpingNote::getVertexCount()
{
	return numNoteVerts;
}



int JumpingNote::getFaceCount()
{
	return numNoteFaces;
}

bool JumpingNote::hasNormals()
{
	return false;
}

void JumpingNote::getFaces(GLuint *f)
{
	for(int i=0; i<getFaceCount(); i++)//iterate over all faces
		{
			for(int j=0; j<3; j++)	//iterate over all coordinates of one face
			{
				f[i*3+j] = NoteFaces[i][j];
			}
		}
}



void JumpingNote::getVerticesAndNormals(GLfloat *v)
{
	for(int i=0; i<getVertexCount(); i++)//iterate over all vertices
		{
			for(int j=0; j<3; j++)	//iterate over all coordinates of one vertex
			{
				v[i*3+j] = NoteVerts[i][j];
				v[i*3+j + getVertexCount()] = NoteVertNorms[i][j];
			}
		}
}



void JumpingNote::getVertices(GLfloat *v)
{
	for(int i=0; i<getVertexCount(); i++)//iterate over all vertices
		{
			for(int j=0; j<3; j++)	//iterate over all coordinates of one vertex
			{
				v[i*3+j] = NoteVerts[i][j];
			}
		}
}

